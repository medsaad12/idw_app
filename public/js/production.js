function jourDiv() {
    let jour = document.getElementsByClassName('jour')[0];
    let periode = document.getElementsByClassName('periode')[0];
    
    periode.style.display = "none" ;
    jour.style.display = "flex" ;
 
}
function periode() {
    let jour = document.getElementsByClassName('jour')[0];
    let periode = document.getElementsByClassName('periode')[0];

    jour.style.display = "none" ;
    periode.style.display = "flex" ;
}

